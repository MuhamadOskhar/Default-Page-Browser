  socialButtons.forEach(button => {
    button.addEventListener('keydown', function (event) {
      // Jika tombol yang ditekan adalah Enter
      if (event.key === 'Enter') {
        // Lakukan sesuatu sesuai dengan tautan tombol media sosial
        window.location.href = this.querySelector('a').href;
      }
    });
  });

  document.addEventListener('keydown', function (event) {
    if (event.key === 'Tab') {
      if (searchInputFocused) {
        searchInputFocused = false;
      }
    }
    // Cek apakah tombol yang ditekan adalah "Ctrl + /"
    if (event.ctrlKey && event.key === '/') {
      if (!searchInputFocused) {
        // Fokuskan elemen input dengan id "search"
        document.querySelector('input[type="text"]').focus();
        searchInputFocused = true;
      } else {
        // Unfokuskan elemen input dengan id "search"
        document.querySelector('input[type="text"]').blur();
        searchInputFocused = false;
      }
    }
    // Cek apakah tombol yang ditekan adalah "Esc"
    if (event.key === 'Escape') {
      document.querySelector('input[type="text"]').blur();
      searchInputFocused = false;
    }
  });

